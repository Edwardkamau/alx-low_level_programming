#include <stdio.h>

/**
 * _isupper - prints 1 if input is uppercase
 * otherwise prints 0
 *
 * @c: character in ASCII code
 *
 * Return: 1 for uppercase. 0 for rest
 */

int _isupper(int c)
{
        if (c >= 'A' && c <= 'A')
        {
        return (1);
        }

        return (0);
}
